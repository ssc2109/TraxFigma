type Screen = "inicio" | "ventas" | "negocio" | "crecer";

interface BottomNavBarProps {
  currentScreen?: Screen;
  onNavigate?: (screen: Screen) => void;
}

export default function BottomNavBar({ currentScreen = "inicio", onNavigate }: BottomNavBarProps) {
  const handleNavigate = (screen: Screen) => {
    if (onNavigate) {
      onNavigate(screen);
    }
  };

  return (
    <div className="bg-[#161616]/95 backdrop-blur-xl relative rounded-[32px] size-full shadow-[0_8px_32px_rgba(0,0,0,0.4)] border border-white/5 flex justify-around items-center px-4" data-name="BottomNavBar">
      <div onClick={() => handleNavigate("inicio")}>
        {currentScreen === "inicio" ? <ButtonTab1InicioActive /> : <ButtonTab1Inicio />}
      </div>
      <div onClick={() => handleNavigate("ventas")}>
        {currentScreen === "ventas" ? <ButtonTab2VentasActive /> : <ButtonTab2Ventas />}
      </div>
      <ButtonTab3TraxFab />
      <div onClick={() => handleNavigate("negocio")}>
        <ButtonTab4Negocio />
      </div>
      <div onClick={() => handleNavigate("crecer")}>
        <ButtonTab5Crecer />
      </div>
    </div>
  );
}